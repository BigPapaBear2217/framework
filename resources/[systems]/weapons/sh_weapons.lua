Hashes = {}
Names = {}
GroupNames = {}

for weapon, settings in pairs(Weapons) do
	Hashes[GetHashKey(weapon)] = weapon
	Names[settings.Name] = weapon
end

for group, settings in pairs(Groups) do
	GroupNames[settings.Name] = group
end

function GetSettings(key)
	if tonumber(key) ~= nil then
		return Weapons[Hashes[key]] or {}
	end
	return Weapons[key] or {}
end
exports("GetSettings", GetSettings)

function GetName(key)
	return GetSettings(key).Name or key
end
exports("GetName", GetName)

function GetAmmo(key)
	return GetSettings(key).Ammo or key
end
exports("GetAmmo", GetAmmo)

function GetAttachments(weapon)
	return  Weapons[weapon].Attachments
end
exports("GetAttachments", GetAttachments)

function GetWeapon(key)
	if type(key) == "number" then
		return Hashes[key]
	end
	return Names[key]
end
exports("GetWeapon", GetWeapon)

function CanAttach(weapon, attachment)
	if not weapon or not attachment then return false end

	local weaponAttachment = Weapons[weapon].Attachments
	if not weaponAttachment then return false end

	if Weapons[weapon].Attachments[attachment] then
		return true
	end
	return false
end
exports("CanAttach", CanAttach)

function GetGroup(group)
	if not group then return end

	if type(group) == "string" then
		return Groups[GroupNames[group] or 0]
	else
		return Groups[group]
	end
end
exports("GetGroup", GetGroup)
